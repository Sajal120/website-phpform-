				<ul>
				<li>2000-2010:</li>
				   <p>Amazon debuted its web-based shopping services in 2002. It was the first significant company to see operating at just 10% of capacity—a practise that was widespread at the time—as a challenge (Foote, 2021).  . </p> <p>
					Amazon introduced Amazon Web Services in 2006, which provides online services to other websites or customers. Amazon Mechanical Turk, one of Amazon Web Services' websites, offers a range of cloud-based services, including storage, processing, and "human intelligence" (Foote, 2021).   </p> <p>
					In 2008, Eucalyptus provided the first AWS API-compatible platform for delivering private clouds. The first open-source software for setting up private and hybrid clouds was made available the same year by NASA's OpenNebula project (Foote, 2021).  </p> 

				    
				<li>2010-2021:</li>
				   <p>Private clouds had been established by organisations like AWS, Microsoft, and OpenStack in 2010, and they were largely functioning. (OpenStack also made a widely used, open-sourced, free DIY cloud platform available to the general public in 2010) (Foote, 2021). </p> <p>
					2011 saw the introduction of the hybrid cloud concept. The flexibility to move workloads back and forth between a private and public cloud is required, as is a decent level of interoperability between the two clouds (Foote, 2021). </p> <p>
					In support of Smarter Planet, IBM unveiled the IBM SmartCloud architecture in 2011. (a cultural thinking project). Then Apple introduced the ICloud, which aims to store more private data (photos, music, videos, etc.) (Foote, 2021). </p> <p>
					The Oracle Cloud, which offers IaaS (Infrastructure-as-a-Service), PaaS (Platform-as-a-Service), and SAAS, was released by Oracle in 2012. (Software-as-a-Service) (Foote, 2021). </p> <p>
					In 2012, CloudBolt was established. This business is recognised for creating a hybrid cloud management platform that assisted businesses in creating, deploying, and managing both private and public clouds. They fixed the issues with public and private cloud compatibility (Foote, 2021). </p> <p>
