					<p>Cloud computing is the next "computer paradigm, where the limitations of computing will be dictated by economic reasoning, rather than technological restrictions alone," according to Professor Ramnath Chellapa of Emory University in 1997 (Foote, 2021).</p>
					<p> In 1999, Salesforce sprang to prominence as a successful use of cloud computing. They utilised it to develop the concept of delivering software to people through the Internet (Foote, 2021). </p>
