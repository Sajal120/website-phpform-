				<h2> Responsible Groups </h2>
					<p>An approach for defining the responsibilities that cloud service users and cloud service providers play in cloud security is the shared responsibility model. According to the shared responsibility paradigm, consumers and service providers are each accountable for maintaining the security of the cloud. To achieve cloud security goals, clients and cloud service providers must collaborate (Harvey, 2018). </p><p>

					Let's imagine the range of security requirements to comprehend the shared responsibility paradigm. Customers of cloud services add up all applicable legal, contractual, and industry standards (such as GDPR, PCI DSS, and contracts) and the result represents all their organization's unique security requirements. These security criteria will aid in ensuring that data is available, private, and has integrity (Harvey, 2018). </p>

